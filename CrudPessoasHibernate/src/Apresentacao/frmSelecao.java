     public void PreencherTabela()
    {
        DefaultTableModel modelo = (DefaultTableModel) tblPessoas.getModel();
        modelo.setNumRows(0);
        for (Pessoa pessoa : atbEstaticos.listaPessoasEstatico)
        {
            modelo.addRow(new Object[] 
            {
                pessoa.getId(), pessoa.getNome(), pessoa.getRg(), pessoa.getCpf()
            });
        }
    }

