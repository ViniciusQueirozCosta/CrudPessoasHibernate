    private void tblPessoasMouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_tblPessoasMouseClicked
    {//GEN-HEADEREND:event_tblPessoasMouseClicked

        int linha = tblPessoas.getSelectedRow();
        atbEstaticos.listaPessoasEstatico.clear();
        Pessoa pessoa = new Pessoa();
        pessoa.setId((int) tblPessoas.getValueAt(linha, 0));
        pessoa.setNome((String) tblPessoas.getValueAt(linha, 1));
        pessoa.setRg((String) tblPessoas.getValueAt(linha, 2));
        pessoa.setCpf((String) tblPessoas.getValueAt(linha, 3));
        atbEstaticos.listaPessoasEstatico.add(pessoa);

    }//GEN-LAST:event_tblPessoasMouseClicked

